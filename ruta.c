size_t get_datos_figura(size_t indice_figura, bool *reflejar, size_t * paleta){

	struct ruta aux = ruta[indice_figura]; 
	
	struct figura_en_ruta figura = figuras_en_ruta[aux.indice_figura];
	
	*reflejar = figura.reflejar;

	size_t indice = ruta[indice_figura].indice_figura;
	
	if (indice == NADA) return 100;
	*paleta = figuras_en_ruta[indice].paleta;
	enum figuras tipo = figuras_en_ruta[indice].figura; 
	
	return tipo;

}

imagen_t *generar_figura(uint16_t rom[],size_t indice_figura){
    
   size_t inicio = tabla_figuras[indice_figura].inicio;
   
   if(inicio == 0)	return NULL;
    
   size_t ancho = tabla_figuras[indice_figura].ancho;
    
   size_t alto = tabla_figuras[indice_figura].alto;	
   
   imagen_t *aux = leer_imagen_rom(rom,ancho,alto,inicio);
   
   if (aux == NULL)	return NULL;

    return aux;
}

float ruta_curvatura (size_t posicion){
    if (posicion < 4200)
        return ruta[posicion].radio_curva;
    else return 0;
}

void obtener_u_c(size_t inicio, float u[]){

	u[0] = 0;
	for (size_t i = 1; i < 96; i++)
	{
		float radio = ruta_curvatura(inicio + i);
		u[i] = u[i - 1] + (radio * exp(0.105 * i - 8.6));
	}
}

void obtener_u_l(float posicion_moto, float u[]){

    u[0] = -posicion_moto;
    
    float alto = 96;
    for (size_t i = 1; i < alto; i++){
      u[i] = -posicion_moto * (float)(alto-i) /alto;
    }
}


